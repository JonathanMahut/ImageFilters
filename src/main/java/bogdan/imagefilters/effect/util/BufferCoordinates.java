package bogdan.imagefilters.effect.util;

public class BufferCoordinates {
	
	private int start;
	private int end;
	
	public BufferCoordinates(int start, int end) {
		super();
		this.start = start;
		this.end = end;
	}

	public int getStart() {
		return start;
	}

	public int getEnd() {
		return end;
	}
}
